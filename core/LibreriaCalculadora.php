    /**
     * Esta función calcula el resultado de sumar dos números pasados como parámetro
     * @param int|float $numero1
     * @param int|float $numero2
     * @return int|float
     */
    function suma($numero1, $numero2){
        return $numero1+$numero2;
    }
    
    /**
     * Esta función calcula el resultado de restar dos números pasados como parámetro
     * @param type $numero1
     * @param type $numero2
     * @return int|float
     */
    function resta($numero1, $numero2){
        return $numero1-$numero2;
    }
    
    /**
     * Esta función calcula el resultado de multiplicar dos números pasados como parámetro
     * @param int|float $numero1
     * @param int|float $numero2
     * @return int|float
     */
    function multiplicacion($numero1, $numero2){
        return $numero1*$numero2;
    }
    
    /**
     * Esta función calcula el resultado de dividir dos números pasados como parámetro
     * @param int|float $numero1
     * @param int|float $numero2
     * @return int|float
     */
    function division($numero1, $numero2){
        return $numero1/$numero2;
    }
?>

